	public static void PrintLogo(LogoType logoType = LogoType.New2)
	{
		string[] toPrint;
		switch (logoType)
		{
			case LogoType.Old:
				toPrint = OldLogoArr;
				break;
			case LogoType.New1:
				toPrint = NewLogo1Arr;
				break;
			case LogoType.New2:
				toPrint = NewLogo2Arr;
				break;
			case LogoType.New3:
			case LogoType.New4:
			default:
				toPrint = OldLogoArr;
				break;
		}

		Console.ForegroundColor = ConsoleColor.DarkYellow;
		foreach (var line in toPrint)
		{
			Console.WriteLine(line);
		}
		Console.ResetColor();
	}

